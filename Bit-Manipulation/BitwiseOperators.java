

public class BitwiseOperators {
    public static void main(String args[]){
        int a=1;
        int b=0;
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);
        System.out.println(~b);
        System.out.println(a<<2);
        System.out.println(a>>2);
    }
}
